# 1. Integer (int)
age = 20
print(age)
print(type(age))


# 2. Float (decimal number)
height = 5.9
print(height)
print(type(height))


# 3. String (text)
name = "AG"
print(name)
print(type(name))


# 4. Boolean (True/False)
is_student = True
print(is_student)
print(type(is_student))


# 5. List (ordered, changeable)
fruits = ["apple", "banana", "mango"]
print(fruits)
print(type(fruits))


# 6. Tuple (ordered, unchangeable)
colors = ("red", "green", "blue")
print(colors)
print(type(colors))


# 7. Set (unordered, unique values)
numbers = {1, 2, 3, 4}
print(numbers)
print(type(numbers))


# 8. Dictionary (key-value pairs)
student = {
    "name": "AG",
    "age": 20
}
print(student)
print(type(student))


# 9. None Type (empty/no value)
data = None
print(data)
print(type(data))