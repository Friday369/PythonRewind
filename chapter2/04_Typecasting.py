# TYPECASTING

# int to float
a = 10
print(float(a))

# float to int
b = 5.9
print(int(b))

# int to string
c = 100
print(str(c))

# string to int
d = "50"
print(int(d) + 10)

# string to float
e = "3.14"
print(float(e))

# int to bool
print(bool(1))   # True
print(bool(0))   # False